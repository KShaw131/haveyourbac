package com.itcs4155.haveyourbac;

public class GraphPoints {
		public double time;
		public double bac;
		public GraphPoints(double time, double bac){
			this.time = time;
			this.bac = bac;
		}
		public double getTime() {
			return time;
		}
		public double getBac() {
			return bac;
		}
}
